// ---- Lookups & helpers ----

const CARDS_BY_ID: Record<string, Card> = Object.fromEntries(
  CARDS.map((c) => [c.id, c]),
);

export function getCard(id: string): Card {
  const card = CARDS_BY_ID[id];
  if (!card) throw new Error(`Unknown card id: ${id}`);
  return card;
}

export function maybeGetCard(id: string): Card | undefined {
  return CARDS_BY_ID[id];
}

// The fixed 12-card starter deck recipe (design doc §2).
export const STARTER_DECK_RECIPE: ReadonlyArray<[cardId: string, count: number]> = [
  ['dink', 3],
  ['drive', 3],
  ['reset', 2],
  ['lob', 1],
  ['smash', 1],
  ['slice', 1],
  ['drop_shot', 1],
];

export const STARTER_CARD_IDS: string[] = CARDS.filter(
  (c) => c.unlockState === 'starter',
).map((c) => c.id);

// Non-starter, non-upgrade cards that can be unlocked / appear in pools.
export const UNLOCKABLE_CARDS: Card[] = CARDS.filter(
  (c) => c.unlockState === 'locked' && !c.isUpgrade,
);

export function getUpgradedCard(cardId: string): Card | undefined {
  const base = CARDS_BY_ID[cardId];
  if (!base?.upgradedId) return undefined;
  return CARDS_BY_ID[base.upgradedId];
}
